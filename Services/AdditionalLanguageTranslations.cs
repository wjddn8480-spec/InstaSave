    public static string Translate(string languageCode, string english)
    {
        if (!Tables.TryGetValue(languageCode, out var table))
            return english;

        if (table.TryGetValue(english, out var exact))
            return exact;

        var result = english;
        foreach (var pair in table.OrderByDescending(p => p.Key.Length))
            result = result.Replace(pair.Key, pair.Value, StringComparison.Ordinal);

        return result;
    }
